texto = "sometimes it is the people no one imagines anything of who do the things that no one can imagine."

# imprime en mayuscula solamente la primer letra de toda la linea
capitalized_texto = texto.capitalize()

# imprime en mayusculas las primeras letras de cada palabra
title_texto = texto.title()

# imprime todo en mayuscula
swapped_texto = texto.swapcase()

print("Capitalized:", capitalized_texto)
print("Titled:", title_texto)
print("Swapped Case:", swapped_texto)