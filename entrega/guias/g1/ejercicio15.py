# nombrando variable
sociedad = "aiPython P1"

# imprimir variable usando print()
print(sociedad)

# Imprimir la longitud de la variable sociedad usando len() y print():
print(len(sociedad))

# Cambiar todos los caracteres a mayúsculas usando el método upper():
sociedad = sociedad.upper()
print(sociedad)

# Cambiar todos los caracteres a minúsculas usando el método lower():
sociedad = sociedad.lower()
print(sociedad)